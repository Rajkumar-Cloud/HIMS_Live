	// Setup lookup options
	public function setupLookupOptions($fld)
	{
		if ($fld->Lookup !== NULL && $fld->Lookup->Options === NULL) {

			// No need to check any more
			$fld->Lookup->Options = [];

			// Set up lookup SQL
			switch ($fld->FieldVar) {
				case "x_TempleteFinalDiagnosis":
					$lookupFilter = function() {
						return "`TemplateType`='TemplateDiagnosis'";
					};
					$lookupFilter = $lookupFilter->bindTo($this);
					break;
				case "x_TemplateTreatment":
					$lookupFilter = function() {
						return "`TemplateType`='Treatment'";
					};
					$lookupFilter = $lookupFilter->bindTo($this);
					break;
				case "x_TemplateOperation":
					$lookupFilter = function() {
						return "`TemplateType`='Operation'";
					};
					$lookupFilter = $lookupFilter->bindTo($this);
					break;
				case "x_TemplateFollowUpAdvice":
					$lookupFilter = function() {
						return "`TemplateType`='FollowUpAdvice '";
					};
					$lookupFilter = $lookupFilter->bindTo($this);
					break;
				case "x_TemplateFollowUpMedication":
					$lookupFilter = function() {
						return "`TemplateType`='FollowUpMedication'";
					};
					$lookupFilter = $lookupFilter->bindTo($this);
					break;
				case "x_TemplatePlan":
					$lookupFilter = function() {
						return "`TemplateType`='TemplatePlan'";
					};
					$lookupFilter = $lookupFilter->bindTo($this);
					break;
				default:
					$lookupFilter = "";
					break;
			}

			// Always call to Lookup->getSql so that user can setup Lookup->Options in Lookup_Selecting server event
			$sql = $fld->Lookup->getSql(FALSE, "", $lookupFilter, $this);

			// Set up lookup cache
			if ($fld->UseLookupCache && $sql <> "" && count($fld->Lookup->ParentFields) == 0 && count($fld->Lookup->Options) == 0) {
				$conn = &$this->getConnection();
				$totalCnt = $this->getRecordCount($sql);
				if ($totalCnt > $fld->LookupCacheCount) // Total count > cache count, do not cache
					return;
				$rs = $conn->execute($sql);
				$ar = [];
				while ($rs && !$rs->EOF) {
					$row = &$rs->fields;

					// Format the field values
					switch ($fld->FieldVar) {
						case "x_Name":
							break;
						case "x_Consultant":
							break;
						case "x_Surgeon":
							break;
						case "x_Anesthetist":
							break;
						case "x_TempleteFinalDiagnosis":
							break;
						case "x_TemplateTreatment":
							break;
						case "x_TemplateOperation":
							break;
						case "x_TemplateFollowUpAdvice":
							break;
						case "x_TemplateFollowUpMedication":
							break;
						case "x_TemplatePlan":
							break;
					}
					$ar[strval($row[0])] = $row;
					$rs->moveNext();
				}
				if ($rs)
					$rs->close();
				$fld->Lookup->Options = $ar;
			}
		}
	}

