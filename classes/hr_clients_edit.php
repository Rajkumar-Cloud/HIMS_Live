	// Validate form
	protected function validateForm()
	{
		global $Language, $FormError;

		// Initialize form error message
		$FormError = "";

		// Check if validation required
		if (!SERVER_VALIDATE)
			return ($FormError == "");
		if ($this->id->Required) {
			if (!$this->id->IsDetailKey && $this->id->FormValue != NULL && $this->id->FormValue == "") {
				AddMessage($FormError, str_replace("%s", $this->id->caption(), $this->id->RequiredErrorMessage));
			}
		}
		if ($this->name->Required) {
			if (!$this->name->IsDetailKey && $this->name->FormValue != NULL && $this->name->FormValue == "") {
				AddMessage($FormError, str_replace("%s", $this->name->caption(), $this->name->RequiredErrorMessage));
			}
		}
		if ($this->details->Required) {
			if (!$this->details->IsDetailKey && $this->details->FormValue != NULL && $this->details->FormValue == "") {
				AddMessage($FormError, str_replace("%s", $this->details->caption(), $this->details->RequiredErrorMessage));
			}
		}
		if ($this->first_contact_date->Required) {
			if (!$this->first_contact_date->IsDetailKey && $this->first_contact_date->FormValue != NULL && $this->first_contact_date->FormValue == "") {
				AddMessage($FormError, str_replace("%s", $this->first_contact_date->caption(), $this->first_contact_date->RequiredErrorMessage));
			}
		}
		if (!CheckDate($this->first_contact_date->FormValue)) {
			AddMessage($FormError, $this->first_contact_date->errorMessage());
		}
		if ($this->created->Required) {
			if (!$this->created->IsDetailKey && $this->created->FormValue != NULL && $this->created->FormValue == "") {
				AddMessage($FormError, str_replace("%s", $this->created->caption(), $this->created->RequiredErrorMessage));
			}
		}
		if (!CheckDate($this->created->FormValue)) {
			AddMessage($FormError, $this->created->errorMessage());
		}
		if ($this->address->Required) {
			if (!$this->address->IsDetailKey && $this->address->FormValue != NULL && $this->address->FormValue == "") {
				AddMessage($FormError, str_replace("%s", $this->address->caption(), $this->address->RequiredErrorMessage));
			}
		}
		if ($this->contact_number->Required) {
			if (!$this->contact_number->IsDetailKey && $this->contact_number->FormValue != NULL && $this->contact_number->FormValue == "") {
				AddMessage($FormError, str_replace("%s", $this->contact_number->caption(), $this->contact_number->RequiredErrorMessage));
			}
		}
		if ($this->contact_email->Required) {
			if (!$this->contact_email->IsDetailKey && $this->contact_email->FormValue != NULL && $this->contact_email->FormValue == "") {
				AddMessage($FormError, str_replace("%s", $this->contact_email->caption(), $this->contact_email->RequiredErrorMessage));
			}
		}
		if ($this->company_url->Required) {
			if (!$this->company_url->IsDetailKey && $this->company_url->FormValue != NULL && $this->company_url->FormValue == "") {
				AddMessage($FormError, str_replace("%s", $this->company_url->caption(), $this->company_url->RequiredErrorMessage));
			}
		}
		if ($this->status->Required) {
			if ($this->status->FormValue == "") {
				AddMessage($FormError, str_replace("%s", $this->status->caption(), $this->status->RequiredErrorMessage));
			}
		}
		if ($this->HospID->Required) {
			if (!$this->HospID->IsDetailKey && $this->HospID->FormValue != NULL && $this->HospID->FormValue == "") {
				AddMessage($FormError, str_replace("%s", $this->HospID->caption(), $this->HospID->RequiredErrorMessage));
			}
		}

		// Return validate result
		$validateForm = ($FormError == "");

		// Call Form_CustomValidate event
		$formCustomError = "";
		$validateForm = $validateForm && $this->Form_CustomValidate($formCustomError);
		if ($formCustomError <> "") {
			AddMessage($FormError, $formCustomError);
		}
		return $validateForm;
	}

