	// Set up foreign keys from filter
	protected function setupForeignKeysFromFilter($f)
	{
		$masterTblVar = Get("t", "");
		if ($masterTblVar == "view_dashboard_summary_details") {
			$find = "`UserName`=";
			$x = strpos($f, $find);
			if ($x !== FALSE) {
				$x += strlen($find);
				$y = strpos($f, " AND ", $x);
				if ($y !== FALSE)
					$val = substr($f, $x, $y-$x);
				else
					$val = "";
				$val = $this->unQuoteValue($val, "DB");
 				$this->UserName->setQueryStringValue($val);
			}
			$find = "`createddate`=";
			$x = strpos($f, $find);
			if ($x !== FALSE) {
				$x += strlen($find);
				$y = strpos($f, " AND ", $x);
				if ($y !== FALSE)
					$val = substr($f, $x, $y-$x);
				else
					$val = "";
				$val = $this->unQuoteValue($val, "DB");
 				$this->createddate->setQueryStringValue($val);
			}
			$find = "`HospID`=";
			$x = strpos($f, $find);
			if ($x !== FALSE) {
				$x += strlen($find);
				$val = substr($f, $x);
				$val = $this->unQuoteValue($val, "DB");
 				$this->HospID->setQueryStringValue($val);
			}
		}
	}

	// Unquote value
	protected function unQuoteValue($val, $dbid)
	{
		if (substr($val,0,1) == "'" && substr($val,strlen($val)-1) == "'") {
			if (GetConnectionType($dbid) == "MYSQL")
				return stripslashes(substr($val, 1, strlen($val)-2));
			else
				return str_replace("''", "'", substr($val, 1, strlen($val)-2));
		} else {
			return $val;
		}
	}

