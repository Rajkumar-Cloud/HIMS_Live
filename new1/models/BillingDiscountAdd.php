    // Validate form
    protected function validateForm()
    {
        global $Language;

        // Check if validation required
        if (!Config("SERVER_VALIDATE")) {
            return true;
        }
        if ($this->Particulars->Required) {
            if (!$this->Particulars->IsDetailKey && EmptyValue($this->Particulars->FormValue)) {
                $this->Particulars->addErrorMessage(str_replace("%s", $this->Particulars->caption(), $this->Particulars->RequiredErrorMessage));
            }
        }
        if ($this->Procedure->Required) {
            if (!$this->Procedure->IsDetailKey && EmptyValue($this->Procedure->FormValue)) {
                $this->Procedure->addErrorMessage(str_replace("%s", $this->Procedure->caption(), $this->Procedure->RequiredErrorMessage));
            }
        }
        if (!CheckNumber($this->Procedure->FormValue)) {
            $this->Procedure->addErrorMessage($this->Procedure->getErrorMessage(false));
        }
        if ($this->Pharmacy->Required) {
            if (!$this->Pharmacy->IsDetailKey && EmptyValue($this->Pharmacy->FormValue)) {
                $this->Pharmacy->addErrorMessage(str_replace("%s", $this->Pharmacy->caption(), $this->Pharmacy->RequiredErrorMessage));
            }
        }
        if (!CheckNumber($this->Pharmacy->FormValue)) {
            $this->Pharmacy->addErrorMessage($this->Pharmacy->getErrorMessage(false));
        }
        if ($this->Investication->Required) {
            if (!$this->Investication->IsDetailKey && EmptyValue($this->Investication->FormValue)) {
                $this->Investication->addErrorMessage(str_replace("%s", $this->Investication->caption(), $this->Investication->RequiredErrorMessage));
            }
        }
        if (!CheckNumber($this->Investication->FormValue)) {
            $this->Investication->addErrorMessage($this->Investication->getErrorMessage(false));
        }

        // Return validate result
        $validateForm = !$this->hasInvalidFields();

        // Call Form_CustomValidate event
        $formCustomError = "";
        $validateForm = $validateForm && $this->formCustomValidate($formCustomError);
        if ($formCustomError != "") {
            $this->setFailureMessage($formCustomError);
        }
        return $validateForm;
    }

