    // Get Row Hash
    public function getRowHash(&$rs)
    {
        if (!$rs) {
            return "";
        }
        $row = ($rs instanceof Recordset) ? $rs->fields : $rs;
        $hash = "";
        $hash .= GetFieldHash($row['Catheter']); // Catheter
        $hash .= GetFieldHash($row['Difficulty']); // Difficulty
        $hash .= GetFieldHash($row['Easy']); // Easy
        $hash .= GetFieldHash($row['Comments']); // Comments
        $hash .= GetFieldHash($row['Doctor']); // Doctor
        $hash .= GetFieldHash($row['Embryologist']); // Embryologist
        return md5($hash);
    }

