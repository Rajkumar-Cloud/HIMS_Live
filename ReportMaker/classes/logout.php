		// User profile
		$UserProfile = new UserProfile();

		// Security
		$Security = new AdvancedSecurity();

		// Global Page Loading event (in userfn*.php)
		Page_Loading();

		// Page Load event
		$this->Page_Load();

		// Check token
		if (!$this->validPost()) {
			echo $ReportLanguage->phrase("InvalidPostRequest");
			$this->terminate();
			exit();
		}

		// Create Token
		$this->createToken();
		$validate = TRUE;
		$username = $Security->currentUserName();

		// Call User LoggingOut event
		$validate = $this->User_LoggingOut($username);
		if (!$validate) {
			$lastUrl = $Security->lastUrl();
			if ($lastUrl == "")
				$lastUrl = "index.php";
			$this->terminate($lastUrl); // Go to last accessed URL
		} else {
			if (ReadCookie("AutoLogin") == "") // Not autologin
				WriteCookie("Username", ""); // Clear user name cookie
			WriteCookie("Password", ""); // Clear password cookie
			WriteCookie("LastUrl", ""); // Clear last URL

			// Call User LoggedOut event
			$this->User_LoggedOut($username);

			// Clean upload temp folder
			CleanUploadTempPaths(session_id());

			// Unset all of the Session variables
			$_SESSION = array();

			// Delete the Session cookie and kill the Session
			if (isset($_COOKIE[session_name()]))
				setcookie(session_name(), '', time()-42000, '/');

			// Finally, destroy the Session
			@session_destroy();

			// If session expired, show expired message
			if (Get("expired") == "1")
				$this->terminate("login.php?expired=1"); // Go to login page
			elseif (Get("deleted") == "1")
				$this->terminate("login.php?deleted=1"); // Go to login page
			else
				$this->terminate("login.php"); // Go to login page
		}
	}

