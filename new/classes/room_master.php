	// Export data in HTML/CSV/Word/Excel/Email/PDF format
	public function exportDocument($doc, $recordset, $startRec = 1, $stopRec = 1, $exportPageType = "")
	{
		if (!$recordset || !$doc)
			return;
		if (!$doc->ExportCustom) {

			// Write header
			$doc->exportTableHeader();
			if ($doc->Horizontal) { // Horizontal format, write header
				$doc->beginExportRow();
				if ($exportPageType == "view") {
					$doc->exportCaption($this->id);
					$doc->exportCaption($this->RoomNo);
					$doc->exportCaption($this->RoomType);
					$doc->exportCaption($this->SharingRoom);
					$doc->exportCaption($this->Amount);
					$doc->exportCaption($this->Status);
					$doc->exportCaption($this->PatientID);
					$doc->exportCaption($this->PatientName);
					$doc->exportCaption($this->MobileNo);
					$doc->exportCaption($this->PatID);
				} else {
					$doc->exportCaption($this->id);
					$doc->exportCaption($this->RoomNo);
					$doc->exportCaption($this->RoomType);
					$doc->exportCaption($this->SharingRoom);
					$doc->exportCaption($this->Amount);
					$doc->exportCaption($this->Status);
				}
				$doc->endExportRow();
			}
		}

		// Move to first record
		$recCnt = $startRec - 1;
		if (!$recordset->EOF) {
			$recordset->moveFirst();
			if ($startRec > 1)
				$recordset->move($startRec - 1);
		}
		while (!$recordset->EOF && $recCnt < $stopRec) {
			$recCnt++;
			if ($recCnt >= $startRec) {
				$rowCnt = $recCnt - $startRec + 1;

				// Page break
				if ($this->ExportPageBreakCount > 0) {
					if ($rowCnt > 1 && ($rowCnt - 1) % $this->ExportPageBreakCount == 0)
						$doc->exportPageBreak();
				}
				$this->loadListRowValues($recordset);

				// Render row
				$this->RowType = ROWTYPE_VIEW; // Render view
				$this->resetAttributes();
				$this->renderListRow();
				if (!$doc->ExportCustom) {
					$doc->beginExportRow($rowCnt); // Allow CSS styles if enabled
					if ($exportPageType == "view") {
						$doc->exportField($this->id);
						$doc->exportField($this->RoomNo);
						$doc->exportField($this->RoomType);
						$doc->exportField($this->SharingRoom);
						$doc->exportField($this->Amount);
						$doc->exportField($this->Status);
						$doc->exportField($this->PatientID);
						$doc->exportField($this->PatientName);
						$doc->exportField($this->MobileNo);
						$doc->exportField($this->PatID);
					} else {
						$doc->exportField($this->id);
						$doc->exportField($this->RoomNo);
						$doc->exportField($this->RoomType);
						$doc->exportField($this->SharingRoom);
						$doc->exportField($this->Amount);
						$doc->exportField($this->Status);
					}
					$doc->endExportRow($rowCnt);
				}
			}

			// Call Row Export server event
			if ($doc->ExportCustom)
				$this->Row_Export($recordset->fields);
			$recordset->moveNext();
		}
		if (!$doc->ExportCustom) {
			$doc->exportTableFooter();
		}
	}

